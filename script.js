nexted.addEventListener("click", function () {
  currentIndex++;
  if (currentIndex === quizData[sectionIndex].questions.length) {
    let scoring = document.createElement("p");
    let complete = document.createElement("p");
    let homebtn = document.createElement("button");
    complete.innerHTML = "Quiz Completed";
    scoring.innerHTML = "Your Score Is: " + score;
    homebtn.innerHTML = "Go To Home";
    homebtn.style.width = "300px";
    homebtn.style.height = "100px";
    homebtn.style.backgroundColor = "green";
    questionselement.appendChild(complete);
    questionselement.appendChild(scoring);
    questionselement.appendChild(homebtn);
    selectedOptionElement.style.display = "none";
    nexted.style.display = "none";
    questionss.style.display = "none";
    for (let j = 0; j < optionButtons.length; j++) {
      optionButtons[j].style.display = "none";
    }
    homebtn.addEventListener("click", function () {
      location.reload();
    });
  } else {
    showQuestion();
    selectedOptionElement.innerHTML = "";
  }
});
function showQuestion() {
  let questionDisplay = quizData[sectionIndex].questions[currentIndex];
  let questionNumber = currentIndex + 1;
  questionss.innerHTML = questionNumber + ". " + questionDisplay.question;
  for (let i = 0; i < optionButtons.length; i++) {
    if (questionDisplay.options[i]) {
      optionButtons[i].innerHTML = questionDisplay.options[i];
      optionButtons[i].style.display = "";
    } else {
      optionButtons[i].style.display = "none";
    }
  }
  for (let i = 0; i < optionButtons.length; i++) {
    optionButtons[i].style.backgroundColor = "";
    for (let j = 0; j < optionButtons.length; j++) {
      optionButtons[j].disabled = false;
    }
    optionButtons[i].addEventListener("click", function () {
      for (let i = 0; i < optionButtons.length; i++) {  
        optionButtons[i].disabled = true;
      }
      if (optionButtons[i].innerHTML === questionDisplay.answer) {
        selectedOptionElement.innerHTML =
          "Correct answer! " + optionButtons[i].innerHTML;
        selectedOptionElement.style.color = "greenyellow";
        selectedOptionElement.style.fontSize = "40px";
        optionButtons[i].style.backgroundColor = "green";
        score++;
      } else {
        selectedOptionElement.innerHTML =
          "Wrong answer! The Correct answer is: " + questionDisplay.answer;
        selectedOptionElement.style.color = "red";
        selectedOptionElement.style.fontSize = "40px";
        optionButtons[i].style.backgroundColor = "red";
        for(let i=0;i<optionButtons.length;i++){
        if(optionButtons[i].innerHTML === questionDisplay.answer)
          optionButtons[i].style.backgroundColor = "green";
          optionButtons[i].style.display="";
        }
      }
    });
  }
}
